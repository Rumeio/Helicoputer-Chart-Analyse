function getXForYanother(desiredY, points, tension = 0.5) {
  let prevPoint = points[0];
  for (let i = 1; i < points.length; i++) {
    const point = points[i];
    if (desiredY <= prevPoint.y && desiredY >= point.y) {
      // Linear interpolation for simplicity, between prevPoint and point
      const diffY = point.y - prevPoint.y;
      const diffX = point.x - prevPoint.x;
      const partialY = desiredY - prevPoint.y;
      return prevPoint.x + (diffX * partialY) / diffY;
    }
    prevPoint = point;
  }
  return null; // desiredX is out of bounds
}
